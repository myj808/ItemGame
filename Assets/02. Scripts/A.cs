  1) 플레이어
    
    - 이동(WASD) + 점프(지면 체크) / 카메라(3인칭 팔로우 또는 1인칭)
    - 애니메이션 최소 3종: Idle / Run(또는 Walk) / Jump
    
  2) 아이템 (랜덤 스폰 포함)
    
    - 최소 개수 K 이상의 아이템을 랜덤으로 배치/스폰
    - 오브젝트 풀을 이용한 아이템 재사용
    - 수집 시 : 점수/남은 수 UI 갱신 + 사운드 또는 이펙트 1종 이상
    - 클리어 조건 : 목표 개수 전부 수집 시 클리어 패널 표시
    
  3) UI
    
    - 상단 현재 수집 / 전체 목표 또는 점수
    - 클리어 패널 (다시 시작 / 종료)
    
  4) 게임 매니저
    
    - 목표 개수/현재 수집 수/게임 상태 관리
    - 스폰 시스템과 연동(초기 스폰/재스폰 호출)
    

