    [개요]
    
    - 목표 : 3D 환경에서 캐릭터를 조작하여 모든 아이템을 수집하면 클리어되는 게임 완성
    - 제출 : 빌드파일 + 30~60초 시연 영상(플레이 → 랜덤 스폰 확인 → 수집 → 클리어) + Git 링크
    
    [학습 목표]
    
    - 3D 물리/충돌(콜라이더/트리거/리지드바디/CharacterController) 이해
    - 랜덤 스폰 설계 (스폰 포인트/영역, 확률, 쿨다운)
    - 아이템 수집 → UI 갱신 → 조건부 클리어 루프 구현
    - Animator로 Idle/Run/Jump 상태 전환
    - GameManager 기반 게임 상태/점수 관리
    
    
